import java.io.Serializable;

/**
 * @author Nanjundan
 * 
 */
public class RainbowComplaintBO implements Serializable {

	private static final long serialVersionUID = 4074879539315095100L;
	
	private String userName;
	private String userPhoneNo;
	private String userCompliantDt;
	private String userInvoiceNo;
	private String issueSummary;

	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}

	/**
	 * @param userName
	 *            the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}

	/**
	 * @return the userPhoneNo
	 */
	public String getUserPhoneNo() {
		return userPhoneNo;
	}

	/**
	 * @param userPhoneNo
	 *            the userPhoneNo to set
	 */
	public void setUserPhoneNo(String userPhoneNo) {
		this.userPhoneNo = userPhoneNo;
	}

	/**
	 * @return the userCompliantDt
	 */
	public String getUserCompliantDt() {
		return userCompliantDt;
	}

	/**
	 * @param userCompliantDt
	 *            the userCompliantDt to set
	 */
	public void setUserCompliantDt(String userCompliantDt) {
		this.userCompliantDt = userCompliantDt;
	}

	/**
	 * @return the userInvoiceNo
	 */
	public String getUserInvoiceNo() {
		return userInvoiceNo;
	}

	/**
	 * @param userInvoiceNo
	 *            the userInvoiceNo to set
	 */
	public void setUserInvoiceNo(String userInvoiceNo) {
		this.userInvoiceNo = userInvoiceNo;
	}

	/**
	 * @return the issueSummary
	 */
	public String getIssueSummary() {
		return issueSummary;
	}

	/**
	 * @param issueSummary
	 *            the issueSummary to set
	 */
	public void setIssueSummary(String issueSummary) {
		this.issueSummary = issueSummary;
	}

}
